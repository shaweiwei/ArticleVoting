package com.av.entity;

public class InspecterInfo {

	private String id;
	private String realName;
	private int age;
	private String sex;
	private boolean hasInsQuali;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getRealName() {
		return realName;
	}
	public void setRealName(String realName) {
		this.realName = realName;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public boolean isHasInsQuali() {
		return hasInsQuali;
	}
	public void setHasInsQuali(boolean hasInsQuali) {
		this.hasInsQuali = hasInsQuali;
	}
	
	
	
}
